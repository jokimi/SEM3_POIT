let week = ["понедельнику", "вторнику", "среде", "четвергу", "пятнице", "субботе", "воскресенью"];
let enterDay = prompt("Введите число: ") - 1;
if (enterDay >= 0 && enterDay <= 6) {
    alert(`Данная цифра соответствует ${week[enterDay]}`);
}
else {
    alert("Номер дня недели введен неправильно!");
}